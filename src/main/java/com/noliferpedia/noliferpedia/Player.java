package com.noliferpedia.noliferpedia;

public class Player {
    private String nickname;
    private String name;
    private int age;
    private String discipline;
    private String role;
    private int winnings;

    public Player(String nickname, String name, int age, String discipline, String role, int winnings) {
        this.nickname = nickname;
        this.name = name;
        this.age = age;
        this.discipline = discipline;
        this.role = role;
        this.winnings = winnings;
    }

}
